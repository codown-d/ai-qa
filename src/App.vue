<script setup lang="ts">
function detectOS() {
  const userAgent = window.navigator.userAgent;
  if (userAgent.indexOf('Windows NT') !== -1) {
    document.body.classList.add('windows');
  } else if (userAgent.indexOf('Mac OS X') !== -1) {
    document.body.classList.add('mac');
  }
}

detectOS()
</script>

